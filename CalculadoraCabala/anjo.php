// Função para encontrar o número do anjo
function buscarNumeroAnjo($dataNascimento, $anjoDataNascimento) {
    $dia = $dataNascimento->format('d');
    $mes = $dataNascimento->format('m');

    // Iterar pelos anjos
    foreach ($anjoDataNascimento as $anjo) {
        // Explode as datas para verificar
        $datas = explode('–', $anjo['Datas']);
        
        // Iterar pelas datas
        foreach ($datas as $data) {
            // Remover espaços em branco
            $data = trim($data);
            // Converter o nome do mês em número e obter o dia
            $partesData = explode(' ', $data);
            $diaAnjo = $partesData[0];
            $mesAnjo = nomeMesParaNumero($partesData[1]);

            // Verificar se dia e mês coincidem
            if ($dia == $diaAnjo && $mes == $mesAnjo) {
                return $anjo['Nº'];
            }
        }
    }

    return "Anjo não encontrado.";
}

// Função auxiliar para converter nome do mês para número
function nomeMesParaNumero($nomeMes) {
    $meses = [
        'Jan' => '01', 'Fev' => '02', 'Mar' => '03', 'Abr' => '04',
        'Mai' => '05', 'Jun' => '06', 'Jul' => '07', 'Ago' => '08',
        'Set' => '09', 'Out' => '10', 'Nov' => '11', 'Dez' => '12'
    ];
    return $meses[$nomeMes];
}

